/*                                                                      =

        - Asigna valor:
            > variable = valor;
          
        - Cálculo + Asignación: 
            > Versión larga: variable1 = variable1 + número/variable2;  //+ - * / %
                _Ejemplo:                                                                                                                                                    */
                int variable4 = 5;
                variable4 = variable4 + 4;
                Console.WriteLine("Versión larga -> variable4 = variable4 + 4, da: " + variable4);
                                             
                                                                                                                                                                        /*
            > Versión corta: variable1 += número/variable2;             //+= -= *= /= %=
                _Ejemplo:                                                                                                                                                          */
                int variable5 = 5;
                variable5 += 4;
                Console.WriteLine("Versión corta -> variable5 += 4, da: " + variable5);