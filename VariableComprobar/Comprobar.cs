/*
 * Comprobar si tiene un valor
    - variable.HasValue

        _Ejemplos       
                                                                                                                                                                                */
        int? i = 0;                     //Valor 0
        Console.WriteLine(i.HasValue);  //Muestra True -> porque SI tiene VALOR la variable

                                                                                                                                                  
        int? j = null;                  //Valor nulo
        Console.WriteLine(j.HasValue);  //Muestra False -> porque NO tiene VALOR la variable






